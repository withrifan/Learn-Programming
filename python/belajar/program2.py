nilai = int(input("Masukkan nilainya: "))

if (nilai >= 86 and nilai <= 100):
    print("Predikat: Memuaskan")
elif (nilai >= 76 and nilai <= 85):
    print("Predikat: Baik")
elif (nilai >= 60 and nilai <= 75):
    print("Predikat: Cukup")
elif (nilai >= 40 and nilai <= 59):
    print("Predikat: Kurang")
elif (nilai < 40):
    print("Predikat: Sangat Kurang")

