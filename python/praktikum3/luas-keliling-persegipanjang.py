# Program menghitung luas & keliling persegi panjang
panjang = 15
lebar = 5.7

luas_persegi_panjang = panjang*lebar
keliling_persegi_panjang = 2*(panjang+lebar)

print("Sebuah persegi panjang memiliki panjang",panjang,"cm, lebar",lebar,"cm dan memiliki luas sebesar",luas_persegi_panjang,"cm dan keliling",keliling_persegi_panjang,"cm")