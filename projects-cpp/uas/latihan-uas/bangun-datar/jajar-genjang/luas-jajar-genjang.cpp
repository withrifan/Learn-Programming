#include <iostream>
using namespace std;

int main()
{
    int a, t, hasil;

    cout << "Masukkan nilai alas : ";
    cin >> a;
    cout << "Masukkan nilai tinggi : ";
    cin >> t;
    hasil = a * t;
    cout << "Rumus luas jajar genjang : L = a x t" << endl;
    cout << "Hasil dari luas jajar genjang : " << hasil << endl;

    return 0;
}