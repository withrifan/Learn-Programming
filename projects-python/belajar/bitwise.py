# operator biner AND
a = 13
b = 37
c = a & b
print(c)

# operator biner OR
d = 13
e = 37
f = d | e
print(f)

# operator biner XOR
g = 13
h = 37
i = g ^ h
print(i)

# Menggeser biner ke kanan sebanyak 2 kali
j = 13
k = 37
print(j << 2)
print(k << 2)

# Menggeser biner ke kiri sebanyak 2 kali
j = 13
k = 37
print(j >> 2)
print(k >> 2)

# operator NOT
l = 10
print(~l)

# operator Negative
m = 10
print(-m)